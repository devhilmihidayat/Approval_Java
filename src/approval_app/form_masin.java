    public void getdata() {

        try {
            Statement stat = (Statement) Conek.GetConnection().createStatement();
            String sql = "Select *from tb_mesin";
            ResultSet res = stat.executeQuery(sql);

            while (res.next()) {
                Object[] obj = new Object[3];
                obj[0] = res.getString("kode_M");
                obj[1] = res.getString("lokasi");
                obj[2] = res.getString("kapasitas");

                model.addRow(obj);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Not success");
        }
    }

