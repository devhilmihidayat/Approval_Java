private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
// TODO add your handling code here:
    try {
        Connection conek = Conek.GetConnection();
        Statement statement = conek.createStatement();
        int result = statement.executeUpdate("insert into tb_mesin values "
                + "('" + tkode.getText()
                + "','" + tlok.getText()
                + "','" + tkap.getText() + "')");
        JOptionPane.showMessageDialog(rootPane, "Berhasil Simpan");
        getdata();
        kosong();
        mati();
        btnsave.setEnabled(false);
        btnnew.setEnabled(true);
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(rootPane, "Gagal simpan");
    }

}//GEN-LAST:event_btnsaveActionPerformed

private void tblmatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblmatMouseClicked
// TODO add your handling code here:
    try {
        int table = tblmat.getSelectedRow();
        String a = tblmat.getValueAt(table, 0).toString();
        String b = tblmat.getValueAt(table, 1).toString();
        String c = tblmat.getValueAt(table, 2).toString();

        tkode.setText(a);
        tlok.setText(b);
        tkap.setText(c);

        btnsave.setEnabled(false);
        btnedit.setEnabled(true);
        btndel.setEnabled(true);
        btnnew.setEnabled(false);
        hidup();
        tkode.setEnabled(false);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Gagal");
    }
}//GEN-LAST:event_tblmatMouseClicked

