 public void getdata(){
     try {
         Statement stat= (Statement)Conek.GetConnection().createStatement();
         String sql = "Select *from tb_user";
         ResultSet res = stat.executeQuery(sql);
         
            while(res.next()){
                Object[] obj=new Object[7];
                obj[0]=res.getString("kode_U");
                obj[1]=res.getString("username");
                obj[2]=res.getString("password");
                obj[3]=res.getString("jabatan");
                obj[4]=res.getString("grade");
                obj[5]=res.getString("dept");
                obj[6]=res.getString("plant");
                
                model.addRow(obj);
            }
     }catch (Exception e){
         JOptionPane.showMessageDialog(rootPane, "Not Tampil");
     }
 }   
