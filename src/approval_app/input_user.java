private void tbluserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbluserMouseClicked
// TODO add your handling code here:
    try {
        int table = tbluser.getSelectedRow();
        String a=tbluser.getValueAt(table, 0).toString();
        String b=tbluser.getValueAt(table, 1).toString();
        String c=tbluser.getValueAt(table, 2).toString();
        String d=tbluser.getValueAt(table, 3).toString();
        String e=tbluser.getValueAt(table, 4).toString();
        String f=tbluser.getValueAt(table, 5).toString();
        String g=tbluser.getValueAt(table, 6).toString();
        
        tkode.setText(a);
        tnama.setText(b);
        tpas.setText(c);
        tjab.setText(d);
        tgrad.setText(e);
        tdep.setText(f);
        tplan.setText(g);
        
        btnsave.setEnabled(false);
        btnedit.setEnabled(true);
        btndelete.setEnabled(true);
        btnnew.setEnabled(false);
        hidup();
        tkode.setEnabled(false);
    }catch (Exception e){
        JOptionPane.showMessageDialog(rootPane,"Gagal");
    }
       
}//GEN-LAST:event_tbluserMouseClicked

